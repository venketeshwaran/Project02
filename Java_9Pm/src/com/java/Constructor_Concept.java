package com.java;

public class Constructor_Concept {


	Constructor_Concept(){
		System.out.println("hi");//constructor
	}
	
	Constructor_Concept(int i){
		System.out.println("hello"+i);//parameterizedconstructor
	}
	
	
	public static void main(String[] args) {
		Constructor_Concept cc = new Constructor_Concept();

		
		
	}
	
	
	
}
