package com.java;

public class Control_Statement {
	
	public static void main(String[] args) {
		
		
		for (int i = 1; i<=3; i++) {
			//int i=1; 1<=3; int i=2; 2<=3; int i=3 ; 3<=3;
			//i=1 , i=2 , i=3
			
			for (int j = 1; j<=3; j++) {
			//int j=1; 1<=3 //int j=2 2<=3 //int j=3 3<=3 
		    //j=1 j=2 j=3
		   //j=1 j=2 j=3
		  //j=1 j=2 j=3
				System.out.println(j);
			}
			System.out.println();
			
		}
	}

}
